namespace Prowl.PaperUI.Themes.Origami;

/// <summary>
/// Size options that control the dimensions of components.
/// Used across all Origami components for consistency.
/// </summary>
public enum OrigamiSize
{
    /// <summary>Small size.</summary>
    Small,
    /// <summary>Medium size.</summary>
    Medium,
    /// <summary>Large size.</summary>
    Large
}

/// <summary>
/// Color variants for components that can use different theme colors.
/// </summary>
public enum OrigamiColor
{
    Primary,
    Secondary,
    Success,
    Warning,
    Danger
}

/// <summary>
/// Border radius options for consistent rounded corners.
/// </summary>
public enum OrigamiRadius
{
    /// <summary>No rounded corners.</summary>
    None,
    /// <summary>Small border radius.</summary>
    Small,
    /// <summary>Medium border radius.</summary>
    Medium,
    /// <summary>Large border radius.</summary>
    Large,
    /// <summary>Fully rounded (circular for square elements).</summary>
    Full
}

